void examplewhile() {

	int i = 0;
	int sum = 0;
	while (i < 10) {

		sum = sum + 2;
		i++;

	}
	
	printf("%i", sum);
}


void examplefor() {


	
	int sum = 0;

	for (int i= 0; i<10; i++) {
		sum = sum + 2;
	}
	
	printf("%i", sum);
}


int main()
{


	examplefor();
	return 0;

}


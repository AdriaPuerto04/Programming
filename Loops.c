// ****** Flow control - loops *******

#include <stdio.h>

//print a name 100 times
void example() {

	int counter = 1;

	while (counter <= 100) {

		printf("cojones\n");
		counter++;

	}
	
}

int main()
{

	example();
	return 0;

}

